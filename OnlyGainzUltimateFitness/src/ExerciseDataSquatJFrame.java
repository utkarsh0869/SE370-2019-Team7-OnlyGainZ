    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        state = true;
        //initiates the timer
        Thread t = new Thread(){
            public void run(){

                for(;;){
                    if(state == true){
                        try{
                            sleep(1000);
                            secondsPassed++;
                            seconds.setText(""+secondsPassed);
                        }
                        catch(Exception e){
                        }
                    }
                    else{
                        break;
                    }
                }

            }
        };
        t.start();
    }//GEN-LAST:event_jButton3ActionPerformed

